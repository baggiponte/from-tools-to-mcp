@app.cell
def _(TOOL_REGISTRY, call_tool, json, tools):
    def ask_horoscope(prompt, client, tool_registry=TOOL_REGISTRY):
        input_list = [{"role": "user", "content": prompt}]

        response = client.responses.create(
            model="gpt-5.2",
            tools=tools,  # the global tools defined in the first cell
            input=input_list,
        )

        # Save function call outputs for subsequent requests
        input_list += response.output

        for item in response.output:
            result = call_tool(item)

            input_list.append(
                {
                    "type": "function_call_output",
                    "call_id": item.call_id,
                    "output": json.dumps(
                        {"tool_name": item.name, "tool_output": result}
                    ),
                }
            )

        print("Final input:")
        print(input_list)

        response = client.responses.create(
            model="gpt-5",
            instructions="Respond only with a horoscope generated by a tool.",
            tools=tools,
            input=input_list,
        )

        # 5. The model should be able to give a response!
        print("Final output:")
        print(response.model_dump_json(indent=2))
        print("\n" + response.output_text)


    ask_horoscope(
        "I am a taurus",
        client,
    )
    return


