@app.cell
def _(Agent):
    import sys

    from pydantic_ai.mcp import MCPServerStdio, MCPServerStreamableHTTP

    local_horoscope_mcp = MCPServerStdio(
        sys.executable,
        args=["server.py"],
        tool_prefix="horoscope",
    )
    public_deepwiki_mcp = MCPServerStreamableHTTP(
        "https://mcp.deepwiki.com/mcp",
        tool_prefix="deepwiki",
    )

    mcp_agent = Agent(
        "openai:gpt-5.2",
        system_prompt="For horoscope requests, use the horoscope MCP tool.",
        toolsets=[local_horoscope_mcp, public_deepwiki_mcp],
    )
    return (mcp_agent,)


